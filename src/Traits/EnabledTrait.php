namespace Kod\Traits;

/**
 * Trait EnabledTrait
 * @package Kod\Traits
 */
trait EnabledTrait
{
    protected $enabled = true;

    /**
     * @return bool
     */
    public function isEnabled(): bool
    {
        return $this->enabled;
    }

    /**
     * @param bool $enabled
     */
    public function setEnabled(bool $enabled)
    {
        $this->enabled = $enabled;
    }
}
